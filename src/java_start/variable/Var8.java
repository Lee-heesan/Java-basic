package java_start.variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b= 127; // -128~127 정수
        short s=32767; // -32768~32767 정수
        int i =2147483647; //-2147483648~2147483647 정수

        long l =9223372036854775807L; // int 형보다 넘어가는 숫자이면  끝에 L을 붙여야함

        //실수
        float f= 10.2f; // 끝에 f
        double d=10.2; // double 더 큰 실수 값을 담을 수 있다. 실수 기본으로 사용한다.
    }
}
